WSB_SYSTEM_PROMPT = """{prompt}""".format(prompt='You are a financial sentiment classifier for Reddit r/WallStreetBets (WSB).\n\nYour task is to classify the sentiment expressed in a single post about a stock, ETF, or cryptocurrency.\n\nWSB posts often include slang, memes, sarcasm, and exaggerated financial language (e.g., YOLO, diamond hands, tendies, rekt, bagholder, rocket emojis 🚀).\n\nRules:\n- Interpret sarcasm and irony based on implied meaning, not literal wording.\n- Focus on sentiment toward the financial asset, not humor or meme content alone.\n- Ignore off-topic content unless it implies bullish or bearish sentiment.\n\nLabels (based ONLY on expected price direction of the asset — ignore the poster\'s tone, profit, or emotional state):\npositive: the post expresses or implies the asset\'s price will rise or has risen (bullish direction)\nnegative: the post expresses or implies the asset\'s price will fall or has fallen (bearish direction) — this includes short positions, puts, or bets against the asset, even if that bet was profitable or the poster sounds happy about it\nneutral: no clear directional expectation about price is expressed (pure facts, genuinely uncertain outcome, or no opinion at all)\n\nIMPORTANT:\n- A profitable trade is NOT automatically positive — a profitable short/put bet is negative, because it reflects a bearish view of the asset.\n- Return only one label (positive, neutral, or negative).\n- Do not explain your answer.\n- Do not output anything except the label.')


def _keyword_hints(text):
    """Returns found bullish/bearish slang-signals to send them to the prompt as context."""
    text_lower = str(text).lower()
    bullish = [kw for kw in BULLISH_MEME_KEYWORDS["strong"] | BULLISH_MEME_KEYWORDS["medium"] if kw in text_lower]
    bearish = [kw for kw in BEARISH_MEME_KEYWORDS["strong"] | BEARISH_MEME_KEYWORDS["medium"] if kw in text_lower]
    return bullish, bearish


def _build_prompt(target):
    bullish, bearish = _keyword_hints(target)
    hint_line = ""
    if bullish or bearish:
        hint_line = (f"\n\n(Detected slang signals — bullish: {bullish or 'none'}, "
                     f"bearish: {bearish or 'none'}. These are surface-level cues only — "
                     f"verify against actual price direction and sarcasm before using them.)")
    parts = [WSB_SYSTEM_PROMPT + hint_line, ""]
    for ex in WSB_FEW_SHOT_EXAMPLES:
        parts.append(f"Post: {ex['text']}\nSentiment: {ex['label']}")
    parts.append(f"Post: {target.strip()}\nSentiment:")
    return "\n\n".join(parts)


def _call_llm(prompt, client):
    for attempt in range(RETRY_LIMIT):
        try:
            response = client.responses.create(model=MODEL, input=prompt, temperature=0)
            raw = response.output_text.strip().capitalize()
            if raw in LABELS:
                return raw
            for label in LABELS:
                if label in raw:
                    return label
            return FAILURE_LABEL
        except RateLimitError:
            time.sleep(RETRY_DELAY * (attempt + 1))
        except APIError as e:
            print(f"[classifier] API error: {e}")
            time.sleep(RETRY_DELAY)
    return None


def _extract_ticker(text):
    match = re.search(r'\$([A-Z]{1,5})\b', str(text))
    return match.group(1) if match else None


def _get_client(client, api_key):
    if client is not None:
        return client
    key = api_key or os.environ.get("OPENAI_API_KEY")
    if not key:
        raise ValueError(
            "No OpenAI client or API key provided. Pass client=OpenAI(api_key=...) "
            "or set the OPENAI_API_KEY environment variable in Replit Secrets."
        )
    return OpenAI(api_key=key)


def _has_short_position_signal(text):
    """True if the text explicitly references a bearish-direction bet (puts, shorting, etc.)."""
    text_lower = str(text).lower()
    return any(kw in text_lower for kw in SHORT_POSITION_KEYWORDS)


def classify_post(text, flair=None, client=None, api_key=None):
    """Classify a single WSB post. Returns dict with sentiment, method, ticker, confidence."""
    ticker = _extract_ticker(text)
    if flair and flair in FLAIR_LABEL_MAP:
        if _has_short_position_signal(text):
            # A "Gain" flair with a puts/short signal means the bet was profitable,
            # but the bet itself was bearish — price direction is down, so Negative,
            # not the FLAIR_LABEL_MAP default. This is a rule override, not the LLM.
            return {"sentiment": "Negative", "method": "rules_based_short_override",
                     "ticker": ticker, "confidence": "high"}
        return {"sentiment": FLAIR_LABEL_MAP[flair], "method": "rules_based",
                 "ticker": ticker, "confidence": "high"}
    _client = _get_client(client, api_key)
    label   = _call_llm(_build_prompt(text), _client)
    return {"sentiment": label or FAILURE_LABEL, "method": "llm_judge",
             "ticker": ticker, "confidence": "model"}


def classify_dataframe(df, text_col="title", flair_col="flair", client=None, api_key=None):
    """Classify a full DataFrame. Adds sentiment, classification_method, ticker columns."""
    from tqdm.auto import tqdm
    _client = _get_client(client, api_key)
    results = []
    for _, row in tqdm(df.iterrows(), total=len(df), desc="Classifying"):
        text  = str(row.get(text_col, ""))
        flair = str(row.get(flair_col, "")) if flair_col else None
        results.append(classify_post(text, flair=flair, client=_client))
    out = df.copy()
    out["sentiment"]             = [r["sentiment"] for r in results]
    out["classification_method"] = [r["method"]    for r in results]
    out["ticker"]                = [r["ticker"]     for r in results]
    return out


