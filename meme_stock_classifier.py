def get_sentiment_summary(df, sentiment_col="sentiment"):
    """Return sentiment counts and percentages — useful for dashboard stat cards.

    Posts with FAILURE_LABEL (parsing/API failures, not real classifications) are
    excluded from the percentage denominator but reported separately so failures
    stay visible instead of silently skewing the Positive/Negative/Neutral split.
    """
    failed_mask  = df[sentiment_col] == FAILURE_LABEL
    failed_count = int(failed_mask.sum())
    valid        = df.loc[~failed_mask]
    counts       = valid[sentiment_col].value_counts()
    total        = len(valid)
    return {
        label: {"count": int(counts.get(label, 0)),
                "pct":   round(counts.get(label, 0) / total * 100, 1) if total else 0}
        for label in LABELS
    } | {
        "total": total,
        "unable_to_label_count": failed_count,
    }


def get_top_tickers(df, n=10):
    """Return top-n most-mentioned tickers with sentiment breakdown.

    FAILURE_LABEL rows are dropped before computing percentages (they aren't a
    real sentiment) but their count per ticker is kept as unable_to_label_count
    so failures don't get silently absorbed into Neutral or skew the split.
    """
    if "ticker" not in df.columns:
        df = df.copy()
        df["ticker"] = df.get("title", pd.Series(dtype=str)).apply(_extract_ticker)
    ticker_df = df.dropna(subset=["ticker"])
    if ticker_df.empty:
        return pd.DataFrame()
    rows = []
    for ticker, group in ticker_df.groupby("ticker"):
        failed_mask  = group["sentiment"] == FAILURE_LABEL
        failed_count = int(failed_mask.sum())
        valid_group  = group.loc[~failed_mask]
        counts       = valid_group["sentiment"].value_counts()
        total        = len(valid_group)
        rows.append({
            "ticker"                : ticker,
            "mention_count"         : len(group),
            "unable_to_label_count" : failed_count,
            "positive_pct"          : round(counts.get("Positive", 0) / total * 100, 1) if total else 0,
            "negative_pct"          : round(counts.get("Negative",  0) / total * 100, 1) if total else 0,
            "neutral_pct"           : round(counts.get("Neutral",   0) / total * 100, 1) if total else 0,
            "avg_score"             : round(group["score"].mean(), 1) if "score" in group else 0,
            "dominant_sentiment"    : counts.idxmax() if not counts.empty else "Unable to label",
        })
    return (pd.DataFrame(rows)
            .sort_values("mention_count", ascending=False)
            .head(n)
            .reset_index(drop=True))